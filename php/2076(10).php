<?php
// Defining two string variables
$greeting = "Hello";
$name = "World";

// Concatenating the strings using the dot (.) operator
$fullMessage = $greeting . " " . $name;

// Displaying the concatenated result
echo $fullMessage;
?>



<!--In PHP, variables are defined using the $ sign followed by the variable name. 
Variables can store different data types such as strings, integers, arrays, etc. 
PHP is a loosely typed language, so there's no need to declare the data type explicitly.-->