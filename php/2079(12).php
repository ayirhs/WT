<?php
// Function to create an array of integers and print its elements
function createAndPrintIntegerArray() {
    // Create an array of integers
    $integerArray = array(1, 2, 3, 4, 5);

    // Print each element of the array
    echo "Array elements:<br>";
    foreach ($integerArray as $element) {
        echo $element . "<br>";
    }    //print r garyo bhane elemnts audaina array[0]=> gardai aucha
}

// Call the function
createAndPrintIntegerArray();
?>
